const 운성순서 = ['장생','목욕','관대','건록','제왕','쇠','병','사','묘','절','태','양'];
const 운성시작 = { 목: 8, 화: 2, 토: 2, 금: 5, 수: 8 }; // 지지 인덱스 — 亥=8(목), 寅=2(화토), 巳=5(금), 申=8(수)
// 수정: 목→亥(11), 화→寅(2), 토→寅(2), 금→巳(5), 수→申(8)
const 운성장생지지idx = { 목: 11, 화: 2, 토: 2, 금: 5, 수: 8 };

export function 십이운성(천간글자, 지지글자) {
  const oh = 천간오행[천간글자];
  if (!oh) return null;
  const startIdx = 운성장생지지idx[oh];
  const jiIdx = 지지.indexOf(지지글자);
  if (jiIdx < 0) return null;
  const offset = (jiIdx - startIdx + 12) % 12;
  return 운성순서[offset];
}

