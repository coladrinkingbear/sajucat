export function 신살판정(일간, 일지, 연지, 월지, 시지, G, Z) {
  const 결과 = { 길신: [], 흉신: [], 중립: [] };
  const allZ = Z; // [연지, 월지, 일지, 시지]
  const 위치명 = ['연지','월지','일지','시지'];

  // 천을귀인
  const 귀인지 = 천을귀인표[일간] || [];
  allZ.forEach((z, i) => {
    if (귀인지.includes(z)) 결과.길신.push({ 명: '천을귀인(天乙貴人)', 위치: 위치명[i], 설명: '어려울 때 귀인이 도와주는 운' });
  });

  // 문창귀인
  const 문창지 = 문창귀인표[일간];
  allZ.forEach((z, i) => {
    if (z === 문창지) 결과.길신.push({ 명: '문창귀인(文昌貴人)', 위치: 위치명[i], 설명: '학문과 문서에 뛰어난 재능' });
  });

  // 천덕귀인
  if (월지) {
    const 천덕 = 천덕표[월지];
    G.forEach((g, i) => {
      if (g === 천덕) 결과.길신.push({ 명: '천덕귀인(天德貴人)', 위치: 위치명[i], 설명: '하늘의 복이 있어 재앙을 피하는 운' });
    });
  }

  // 월덕귀인
  if (월지) {
    const 월덕 = 월덕표[월지];
    G.forEach((g, i) => {
      if (g === 월덕) 결과.길신.push({ 명: '월덕귀인(月德貴人)', 위치: 위치명[i], 설명: '달의 은덕으로 조력자가 있는 운' });
    });
  }

  // 건록
  const 건록지 = 건록표[일간];
  allZ.forEach((z, i) => {
    if (z === 건록지) 결과.길신.push({ 명: '건록(建祿)', 위치: 위치명[i], 설명: '재물이 안정되고 자립하는 운' });
  });

  // 역마
  const 역마지 = 역마표[일지];
  allZ.forEach((z, i) => {
    if (z === 역마지 && i !== 2) 결과.중립.push({ 명: '역마(驛馬)', 위치: 위치명[i], 설명: '이동과 변화가 많은 운' });
  });

  // 도화
  const 도화지 = 도화표[일지];
  allZ.forEach((z, i) => {
    if (z === 도화지 && i !== 2) 결과.중립.push({ 명: '도화(桃花)', 위치: 위치명[i], 설명: '이성에 대한 매력과 인연이 강한 운' });
  });

  // 화개
  const 화개지 = 화개표[일지];
  allZ.forEach((z, i) => {
    if (z === 화개지 && i !== 2) 결과.중립.push({ 명: '화개(華蓋)', 위치: 위치명[i], 설명: '학문, 종교, 예술에 천부적 소양' });
  });

  // 양인
  const 양인지 = 양인표[일간];
  allZ.forEach((z, i) => {
    if (z === 양인지) 결과.흉신.push({ 명: '양인(羊刃)', 위치: 위치명[i], 설명: '기운이 극강하여 양날의 검 같은 운' });
  });

  // 괴강
  if (괴강일주.includes(일간 + 일지)) {
    결과.중립.push({ 명: '괴강(魁罡)', 위치: '일주', 설명: '극단적 성격, 강한 결단력과 카리스마' });
  }

  // 원진
  const 원진지 = 원진표[일지];
  allZ.forEach((z, i) => {
    if (z === 원진지 && i !== 2) 결과.흉신.push({ 명: '원진(怨嗔)', 위치: 위치명[i], 설명: '미워도 끊기 어려운 악연의 기운' });
  });

  // 겁살
  const 겁살지 = 겁살표[일지];
  allZ.forEach((z, i) => {
    if (z === 겁살지 && i !== 2) 결과.흉신.push({ 명: '겁살(劫殺)', 위치: 위치명[i], 설명: '갑작스러운 재물 손실이나 사고 주의' });
  });

  // 귀문
  const 귀문지 = 귀문표[일지];
  allZ.forEach((z, i) => {
    if (z === 귀문지 && i !== 2) 결과.흉신.push({ 명: '귀문(鬼門)', 위치: 위치명[i], 설명: '영적 감수성이 높고 정신적 고통이 따를 수 있음' });
  });

  // 천라지망
  const 라망 = 천라지망체크(allZ);
  라망.forEach(r => 결과.흉신.push({ 명: r, 위치: '지지', 설명: '얽히고설킨 운으로 구설과 관재에 주의' }));

  // 고신
  if (연지) {
    const 고신지 = 고신표[연지];
    allZ.forEach((z, i) => {
      if (z === 고신지 && i !== 0) 결과.중립.push({ 명: '고신(孤辰)', 위치: 위치명[i], 설명: '고독한 운, 독립적 성향' });
    });
  }

  // 과숙
  if (연지) {
    const 과숙지 = 과숙표[연지];
    allZ.forEach((z, i) => {
      if (z === 과숙지 && i !== 0) 결과.중립.push({ 명: '과숙(寡宿)', 위치: 위치명[i], 설명: '외로운 운, 배우자 인연에 주의' });
    });
  }

  return 결과;
}

