// 2-1. 통근 판정
export function 통근판정(일간, Z) {
  const 일간오행 = 천간오행[일간];
  let 총점 = 0;
  const 상세 = [];

  Z.forEach((z, i) => {
    const 장간들 = 지장간[z] || [];
    장간들.forEach(jg => {
      if (천간오행[jg.간] === 일간오행) {
        const 가중치 = jg.등급 === '본기' ? 3 : jg.등급 === '중기' ? 2 : 1;
        const 위치보너스 = i === 2 ? 1.5 : 1; // 일지 통근은 1.5배
        총점 += 가중치 * 위치보너스;
        상세.push({ 지지: z, 장간: jg.간, 등급: jg.등급, 가중치: 가중치 * 위치보너스 });
      }
      // 같은 글자(비견)도 통근
      if (jg.간 === 일간) {
        const 가중치 = jg.등급 === '본기' ? 3 : jg.등급 === '중기' ? 2 : 1;
        const 위치보너스 = i === 2 ? 1.5 : 1;
        // 중복 방지: 오행이 같은 경우는 위에서 이미 카운트
        if (천간오행[jg.간] !== 일간오행) {
          총점 += 가중치 * 위치보너스;
          상세.push({ 지지: z, 장간: jg.간, 등급: jg.등급, 가중치: 가중치 * 위치보너스 });
        }
      }
    });
  });

  return { 총점, 상세, 통근여부: 총점 > 0 };
}

// 2-2. 천간 생부 점수 (다른 천간이 일간을 생하거나 같은 오행인지)
export function 천간생부점수(일간, G) {
  const 상생 = { 목:'수', 화:'목', 토:'화', 금:'토', 수:'금' };
  const 일oh = 천간오행[일간];
  let 점수 = 0;

  G.forEach((g, i) => {
    if (i === 2) return; // 일간 자신 제외
    if (g === '?' || !g) return;
    const gOh = 천간오행[g];
    if (gOh === 일oh) 점수 += 2; // 비겁
    else if (상생[일oh] === gOh) 점수 += 1.5; // 인성(나를 생)
  });

  return 점수;
}

// 2-3. 지지 세력 점수 (지장간 전체에서 아군 비율)
export function 지지세력점수(일간, Z) {
  const 상생 = { 목:'수', 화:'목', 토:'화', 금:'토', 수:'금' };
  const 일oh = 천간오행[일간];
  let 아군점 = 0, 적군점 = 0;

  Z.forEach(z => {
    const 장간들 = 지장간[z] || [];
    장간들.forEach(jg => {
      const jgOh = 천간오행[jg.간];
      const w = jg.비율 / 100;
      if (jgOh === 일oh || 상생[일oh] === jgOh) 아군점 += w;
      else 적군점 += w;
    });
  });

  const total = 아군점 + 적군점;
  return total > 0 ? (아군점 / total) * 10 : 5; // 0~10 스케일
}

// 2-4. 강약 종합 판정 (Phase 2의 핵심)
export function 강약판정(일간, 월지, G, Z) {
  // 1) 월령 (40%)
  const 월령 = 월령세력(일간, 월지);
  const 월령점수 = 월령.점수; // 0~4

  // 2) 통근 (30%)
  const 통근 = 통근판정(일간, Z);
  const 통근점수 = Math.min(통근.총점, 10); // cap at 10

  // 3) 천간 생부 (15%)
  const 천간점수 = 천간생부점수(일간, G);

  // 4) 지지 세력 (15%)
  const 지지점수 = 지지세력점수(일간, Z);

  // 종합 (100점 만점으로 환산)
  const 종합 = (월령점수 / 4 * 40) + (통근점수 / 10 * 30) + (천간점수 / 6 * 15) + (지지점수 / 10 * 15);

  let 강약, 등급;
  if (종합 >= 65) { 강약 = '신강'; 등급 = '극신강'; }
  else if (종합 >= 45) { 강약 = '신강'; 등급 = '신강'; }
  else if (종합 >= 30) { 강약 = '신약'; 등급 = '신약'; }
  else { 강약 = '신약'; 등급 = '극신약'; }

  return {
    강약, 등급, 종합점수: Math.round(종합),
    월령: { ...월령, 비중: '40%' },
    통근: { ...통근, 비중: '30%' },
    천간생부: { 점수: 천간점수, 비중: '15%' },
    지지세력: { 점수: Math.round(지지점수 * 10) / 10, 비중: '15%' }
  };
}

// 2-5. 용신 판정 (억부 + 조후)
export function 용신판정(일간, 월지, 강약결과) {
  const 상생 = { 목:'수', 화:'목', 토:'화', 금:'토', 수:'금' };
  const 상극 = { 목:'금', 화:'수', 토:'목', 금:'화', 수:'토' };
  const 피극 = { 목:'토', 화:'금', 토:'수', 금:'목', 수:'화' }; // 내가 극하는 것
  const 설기 = { 목:'화', 화:'토', 토:'금', 금:'수', 수:'목' }; // 내가 생하는 것
  const 일oh = 천간오행[일간];

  // 억부용신
  let 억부용신, 억부근거;
  if (강약결과.강약 === '신강') {
    // 신강 → 설기(식상) 또는 극(관살)이 용신
    억부용신 = 설기[일oh]; // 우선 설기
    억부근거 = '신강하여 기운을 설(泄)하는 ' + 억부용신 + '이 용신';
  } else {
    // 신약 → 생(인성) 또는 비겁이 용신
    억부용신 = 상생[일oh]; // 우선 인성
    억부근거 = '신약하여 기운을 보(補)하는 ' + 억부용신 + '이 용신';
  }

  // 조후용신 (계절에 따라)
  const 계절 = 월지계절[월지];
  let 조후용신 = null, 조후근거 = '';
  if (계절 === '겨울' && (일oh === '목' || 일oh === '화')) {
    조후용신 = '화';
    조후근거 = '겨울에 태어나 따뜻한 火가 조후로 필요';
  } else if (계절 === '여름' && (일oh === '금' || 일oh === '수')) {
    조후용신 = '수';
    조후근거 = '여름에 태어나 시원한 水가 조후로 필요';
  } else if (계절 === '겨울' && 일oh === '토') {
    조후용신 = '화';
    조후근거 = '겨울 토는 얼어붙으니 火로 녹여야 함';
  } else if (계절 === '여름' && 일oh === '화') {
    조후용신 = '수';
    조후근거 = '여름 화는 과열되니 水로 식혀야 함';
  }

  // 최종 용신 = 억부용신 (조후와 같으면 더 강력)
  const 용신 = 억부용신;
  const 희신 = 상생[용신] || 용신; // 용신을 생해주는 오행
  const 기신 = 상극[용신] || ''; // 용신을 극하는 오행

  return {
    용신, 희신, 기신,
    억부: { 오행: 억부용신, 근거: 억부근거 },
    조후: 조후용신 ? { 오행: 조후용신, 근거: 조후근거 } : null,
    조후일치: 조후용신 === 억부용신
  };
}

