// 5-12. 대운 전환점 서사 (동적 생성)
// ──────────────────────────────────────────
export function 대운전환점서사(대운길흉결과) {
  const 블록들 = [];

  // 가장 좋은 대운 찾기
  const 최고 = 대운길흉결과.reduce((best, d) => d.점수 > best.점수 ? d : best, { 점수: 0 });
  if (최고.점수 >= 65) {
    블록들.push({
      type: 'reversal',
      title: '전환점이 온다',
      text: 최고.시작나이 + '대 무렵(' + 최고.간지 + ' 대운)에 용신의 기운이 들어와 큰 전환점이 될 수 있네. 이 시기를 잘 준비하면 인생이 크게 달라질 것이네.'
    });
  }

  // 가장 나쁜 대운 경고
  const 최저 = 대운길흉결과.reduce((worst, d) => d.점수 < worst.점수 ? d : worst, { 점수: 100 });
  if (최저.점수 <= 35) {
    블록들.push({
      type: 'warning',
      title: '주의해야 할 시기',
      text: 최저.시작나이 + '대 무렵(' + 최저.간지 + ' 대운)에 기신의 기운이 강해지니 무리한 투자나 큰 결정은 삼가는 것이 좋겠네.'
    });
  }

  return 블록들;
}

// ──────────────────────────────────────────
// 종합 서사 생성 함수
// ──────────────────────────────────────────
export function 종합서사생성(result) {
  const blocks = [];

  // 1. 일간 성정
  const 성정 = 일간성정[result.일간];
  if (성정) {
    blocks.push({ type: 'ilgan', title: 성정.물상 + '의 기운 — ' + 성정.핵심, text: 성정.설명 });
  }

  // 2. 강약
  const 강약 = 강약서사[result.강약등급 || result.강약];
  if (강약) {
    blocks.push({ type: 'gangak', title: 강약.제목, text: 강약.설명 });
  }

  // 3. 격국
  const 격국 = 격국서사[result.격국?.replace('격', '') + '격'] || 격국서사[result.격국];
  if (격국) {
    blocks.push({ type: 'gyeokguk', title: result.격국 + ' — ' + 격국.핵심, text: 격국.설명 });
  }

  // 4. 특수 조합 (해당하는 것 전부)
  Object.values(특수조합).forEach(combo => {
    try {
      if (combo.조건(result)) {
        blocks.push({ type: 'special', title: combo.제목, text: combo.설명 });
      }
    } catch (e) {}
  });

  // 5. 충
  if (result.충) {
    result.충.forEach(c => {
      const key = [c.지1, c.지2].sort().join('');
      // 정렬된 키로 찾기
      const 서사 = 충서사[c.지1 + c.지2] || 충서사[c.지2 + c.지1];
      const 위치 = 충위치서사[c.위치];
      if (서사) {
        blocks.push({ type: 'chung', title: c.지1 + c.지2 + '충(' + 서사.핵심 + ')', text: 서사.설명 + (위치 ? ' ' + 위치 : '') });
      }
    });
  }

  // 6. 합
  if (result.합) {
    result.합.forEach(h => {
      // "子丑합(토)" 형태에서 키 추출
      const m = h.match(/^(..)합/);
      if (m) {
        const 서사 = 육합서사[m[1]];
        if (서사) blocks.push({ type: 'hap', title: h, text: 서사 });
      }
      const m2 = h.match(/^(...)삼합/);
      if (m2) {
        const 서사 = 삼합서사[m2[1]];
        if (서사) blocks.push({ type: 'hap', title: h, text: 서사 });
      }
    });
  }

  // 7. 천간합
  if (result.천간합) {
    result.천간합.forEach(h => {
      const key = h.간1 + h.간2;
      const 서사 = 천간합서사[key] || 천간합서사[h.간2 + h.간1];
      if (서사) blocks.push({ type: 'cheongan_hap', title: key + '합(' + h.명칭 + ')', text: 서사 });
    });
  }

  // 8. 형
  if (result.형) {
    result.형.forEach(h => {
      const 서사 = 형서사[h];
      if (서사) blocks.push({ type: 'hyeong', title: h, text: 서사 });
    });
  }

  // 9. 해/파
  if (result.해 && result.해.length > 0) {
    blocks.push({ type: 'hae', title: '육해(六害)', text: 해서사 });
  }
  if (result.파 && result.파.length > 0) {
    blocks.push({ type: 'pa', title: '파(破)', text: 파서사 });
  }

  // 10. 신살
  if (result.신살) {
    [...result.신살.길신, ...result.신살.흉신, ...result.신살.중립].forEach(s => {
      const 서사 = 신살서사[s.명];
      if (서사) blocks.push({ type: s.명.includes('귀인') || s.명.includes('건록') ? 'gilsin' : s.명.includes('양인') || s.명.includes('겁살') || s.명.includes('원진') || s.명.includes('귀문') ? 'hyungsin' : 'neutral_sinsal', title: s.명 + ' (' + s.위치 + ')', text: 서사 });
    });
  }

  // 11. 공망
  if (result.공망) {
    if (result.공망.해당.length > 0) {
      result.공망.해당.forEach(k => {
        const 서사 = 공망서사[k.위치];
        if (서사) blocks.push({ type: 'gongmang', title: k.지지 + ' 공망 (' + k.위치 + ')', text: 서사 });
      });
    } else {
      blocks.push({ type: 'gongmang', title: '공망 없음', text: 공망서사.없음 });
    }
  }

  // 12. 오행 전무/과다
  if (result.오행) {
    (result.오행.전무 || []).forEach(oh => {
      const 서사 = 오행전무서사[oh];
      if (서사) blocks.push({ type: 'oheng_zero', title: oh + '(木火土金水) 전무', text: 서사 });
    });
    (result.오행.과다 || []).forEach(oh => {
      const 서사 = 오행과다서사[oh];
      if (서사) blocks.push({ type: 'oheng_excess', title: oh + '(木火土金水) 과다', text: 서사 });
    });
  }

  // 13. 대운 전환점
  if (result.대운길흉) {
    blocks.push(...대운전환점서사(result.대운길흉));
  }

  return blocks;
}
