// ── 도우미: 십성 조합 감지 ──
export function 십성조합감지(ss, 강약) {
  const results = [];
  for (const [key, combo] of Object.entries(십성조합)) {
    if (combo.조건(ss)) {
      const isWeak = 강약.indexOf('신약') >= 0 || 강약.indexOf('극신약') >= 0;
      results.push({
        key,
        제목: combo.제목,
        해설: combo.해설,
        추가: isWeak ? combo.신약 : combo.신강
      });
    }
  }
  return results;
}

